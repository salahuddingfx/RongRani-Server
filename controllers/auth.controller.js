const jwt = require('jsonwebtoken');
const User = require('../models/User');
const { sendEmail } = require('../services/emailService');

// Generate JWT token
const generateToken = (id) => {
  return jwt.sign({ id }, process.env.JWT_SECRET, {
    expiresIn: process.env.JWT_EXPIRE || '30d',
  });
};

// @desc    Register user
// @route   POST /api/auth/register
// @access  Public
const register = async (req, res) => {
  try {
    const { name, email, password } = req.body;

    // Check if user exists
    const userExists = await User.findOne({ email });
    if (userExists) {
      return res.status(400).json({ message: 'User already exists' });
    }

    // Create user
    const user = await User.create({
      name,
      email,
      password,
    });

    // Send welcome email
    try {
      console.log('📧 Attempting to send welcome email to:', email);
      await sendEmail(email, 'Welcome to RongRani', 'welcome', { name });
      console.log('✅ Welcome email sent successfully to:', email);

      // Send admin notification about new user
      console.log('📧 Sending new user notification to admin...');
      await sendEmail(
        process.env.SUPER_ADMIN_EMAIL || 'info.rongrani@gmail.com',
        '🆕 New User Registered - RongRani',
        'adminNewUser',
        { userName: name, userEmail: email, registeredAt: new Date().toLocaleString() }
      );
      console.log('✅ Admin notification sent');
    } catch (emailError) {
      console.error('❌ Welcome email failed:', emailError.message);
      console.error('Full error:', emailError);
    }

    const token = generateToken(user._id);

    res.status(201).json({
      _id: user._id,
      name: user.name,
      email: user.email,
      role: user.role,
      token,
    });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// @desc    Login user
// @route   POST /api/auth/login
// @access  Public
const login = async (req, res) => {
  try {
    const { email, password } = req.body;

    const user = await User.findOne({ email }).select('+password');
    if (!user || !(await user.comparePassword(password))) {
      return res.status(401).json({ message: 'Invalid credentials' });
    }

    // Update last login
    user.lastLogin = new Date();
    await user.save();

    const token = generateToken(user._id);

    console.log('🔓 User logged in:', {
      email: user.email,
      role: user.role,
      token: token.substring(0, 20) + '...'
    });

    res.json({
      _id: user._id,
      name: user.name,
      email: user.email,
      role: user.role,
      token,
    });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// @desc    Get current user
